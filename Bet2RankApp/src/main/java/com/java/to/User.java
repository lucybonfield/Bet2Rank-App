package com.java.to;

public class User {
	private String username;
	private String password;
	private String email;
	private String betsPlaced;
	private String betsWon;
	private String successRate;
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getBetsPlaced() {
		return betsPlaced;
	}
	public void setBetsPlaced(String betsPlaced) {
		this.betsPlaced = betsPlaced;
	}
	public String getBetsWon() {
		return betsWon;
	}
	public void setBetsWon(String betsWon) {
		this.betsWon = betsWon;
	}
	public String getSuccessRate() {
		return successRate;
	}
	public void setSuccessRate(String successRate) {
		this.successRate = successRate;
	}
	
	

}
